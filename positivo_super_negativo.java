public class positivo_super_negativo {
    
    public static void main(String[] args){
    
        int num = -15;
        if (num > 0)
        {
            System.out.println("numero positivo");
        }

        else if (num == 0){
            System.out.println("cero");
        }

        else {
            System.out.println("numero negativo");
        }


    }
}